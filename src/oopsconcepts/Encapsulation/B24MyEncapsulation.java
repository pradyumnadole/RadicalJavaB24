package oopsconcepts.Encapsulation;

public class B24MyEncapsulation {

	private String sname;
	private int empid;
	private String address;
	private int mob;
	private String education;
	private String Hobby;
	
	public String getHobby() {
		return Hobby;
	}
	public void setHobby(String hobby1) {
		Hobby = hobby1;
	}
	public String getSname() {
		return sname;
	}
	public void setSname(String sname1) {
		sname = sname1;
	}
	public int getEmpid() {
		return empid;
	}
	public void setEmpid(int empid1) {
		empid = empid1;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address1) {
		address = address1;
	}
	public int getMob() {
		return mob;
	}
	public void setMob(int mob1) {
		mob = mob1;
	}
	public String getEducation() {
		return education;
	}
	public void setEducation(String education1) {
		education = education1;
	}

}
