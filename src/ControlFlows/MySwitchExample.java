package ControlFlows;

public class MySwitchExample {

	public static void main(String[] args) {
		int i = 500;
		switch (i) {
		case 100:
			System.out.println("Priting 100");
			break;
		case 200:
			System.out.println("Priting 200");
			break;
		case 300:
			System.out.println("Priting 300");
			break;
		default:
			System.out.println("Priting default");
			break;
		}

	}

}
