package Excercise;

public class MySwitchProgram1 {

	public static void main(String[] args) {
		String color = "Blue";
		switch (color) {
		case "Red":
			System.out.println("Entered color is Red");
			break;
		case "Yellow":
			System.out.println("Entered color is Yellow");
			break;
		case "Blue":
			System.out.println("Entered color is Blue");
			break;
		case "Green":
			System.out.println("Entered color is Green");
			break;
		default:
			System.out.println("No color entered");
		}

	}

}
