package Excercise;

public class MyForLoopProgram1 {

	public static void main(String[] args) {
		for (int i=0;i<10;i++) {
			System.out.println("value of i is " +i);
		}
		System.out.println("=====================");
		
		for (int i=0,j=9;i<10;i++, j--) {
			System.out.println(i+"     "+j);
		}

	}

}
